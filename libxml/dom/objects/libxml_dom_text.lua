function libxml.dom.createText(pText)
    local self = libxml.dom.createCharacterData(pText)    
    
    --===================================================================
    -- PROPERTIES                                                       =
    --===================================================================
    self.nodeName = "#text"    
    ---------------------------------------------------------------------
    self.nodeType = 3
    ---------------------------------------------------------------------
    self.nodeDesc = libxml.dom.nodeTypes[3]
    ---------------------------------------------------------------------
    
    --===================================================================
    -- MUTATORS                                                         =
    --===================================================================
    
    --===================================================================
    -- METHODS                                                            =    
    --===================================================================        
    
    return self
end